public class Global implements java.io.Serializable {

	// Fields

	private Integer id;
	private Double output;
	private Double hkm;
	//分页属性,不做映射
	private Integer page;//表示当前是第几页
	private Integer rows;//每页显示多少条记录
	
	public Integer getPage() {
		return page;
	}
	public void setPage(Integer page) {
		this.page = page;
	}
	public Integer getRows() {
		return rows;
	}
	public void setRows(Integer rows) {
		this.rows = rows;
	}
	//分页属性,不做映射
	// Constructors

	/** default constructor */
	public Global() {
	}

	/** full constructor */
	public Global(Double output, Double hkm) {
		this.output = output;
		this.hkm = hkm;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Double getOutput() {
		return this.output;
	}

	public void setOutput(Double output) {
		this.output = output;
	}

	public Double getHkm() {
		return this.hkm;
	}

	public void setHkm(Double hkm) {
		this.hkm = hkm;
	}

}